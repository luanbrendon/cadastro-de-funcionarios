    private void jFormattedTextField_datanascimentoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField_datanascimentoKeyReleased
        // TODO add your handling code here:
        DateTimeFormatter date = DateTimeFormatter.ofPattern("dd/MM/yyyy");
    LocalDate dataNascimento = LocalDate.parse(jFormattedTextField_datanascimento.getText(), date);
    LocalDate dataAtual = LocalDate.now();
    Period periodo = Period.between(dataNascimento,dataAtual);
    String Resultado = (periodo.getYears()+ " Anos "  + periodo.getMonths() + " Meses"+" e " +  periodo.getDays() + " dias");
    jTextField_idade.setText(Resultado);
    }//GEN-LAST:event_jFormattedTextField_datanascimentoKeyReleased

