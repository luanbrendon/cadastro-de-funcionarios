    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        try {
            // recebendo e validando dados da interface gr�fica.
            String nome = "";
            String cpf = "";
            String telefone = "";
            String senha = "";
            String datadenascimento = "";
            String codigo = "";
            String rg = "";
            String endereco = "";

            if (this.field_nome.getText().length() < 2) {
                throw new Mensagens("O Nome deve conter ao menos 2 caracteres.");
            } else {
                nome = this.field_nome.getText();
            }

            if (this.jFormattedTextField_cpf.getText().length() < 11) {
                throw new Mensagens("Número de digitos no CPF insuficiente.");
            } else {
                cpf = this.jFormattedTextField_cpf.getText();
            }
            
            if (this.jFormattedTextField_telefone.getText().length() < 2) {
                throw new Mensagens("O Telefone deve conter ao menos 2 caracteres.");
            } else {
                telefone = this.jFormattedTextField_telefone.getText();
            }

            if (this.jPasswordField_senha.getText().length() <= 1) {
                throw new Mensagens("A Senha deve ser número e maior que um.");
            } else {
                senha = this.jPasswordField_senha.getText();
            }
            
            if (this.jFormattedTextField_datanascimento.getText().length() <= 5) {
                throw new Mensagens("A data de nascimento deve ser um número maior que 0.");
            } else {
                datadenascimento = this.jFormattedTextField_datanascimento.getText();
                
            }
            
            if (this.jFormattedTextField_codigo.getText().length() <= 6) {
                throw new Mensagens("O Codigo deve ser um número maior que 6.");
            } else {
                codigo = this.jFormattedTextField_codigo.getText();
                
            }
            
            if (this.jFormattedTextField_rg.getText().length() <= 6) {
                throw new Mensagens("O RG deve ser um número maior que 6.");
            } else {
                rg = this.jFormattedTextField_rg.getText();
                
            }
            
            if (this.jTextField_endereco.getText().length() <= 0) {
                throw new Mensagens("O endereco deve ser maior que 0.");
            } else {
                endereco = this.jTextField_endereco.getText();
                
            }

            // envia os dados para o Controlador cadastrar
            if (this.objfuncionario.InsertFuncionarioBD(cpf, telefone, senha, nome, datadenascimento, codigo, rg, endereco)) {
                JOptionPane.showMessageDialog(rootPane, "Funcionario Cadastrado com Sucesso!");

                // limpa campos da interface
                this.field_nome.setText("");
                this.jFormattedTextField_cpf.setText("");
                this.jFormattedTextField_telefone.setText("");   
                this.jPasswordField_senha.setText("");
                this.jFormattedTextField_datanascimento.setText("");
                this.jFormattedTextField_codigo.setText("");
                this.jFormattedTextField_rg.setText("");
                this.jTextField_endereco.setText("");
                

            }

            System.out.println(this.objfuncionario.getMinhaLista().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número.");
        }
    }//GEN-LAST:event_btn_cadastrarActionPerformed

